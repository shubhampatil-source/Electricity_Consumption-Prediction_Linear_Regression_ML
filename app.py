import streamlit as st
import numpy as np
import joblib

model = joblib.load("model.pkl")
scaler = joblib.load("scaler.pkl")

st.title("⚡ Electricity Consumption Predictor")

st.write("Enter details to predict residential electricity consumption")

# User Inputs
population = st.number_input("Population (in thousands)", min_value=1.0)
cooling = st.number_input("Cooling Degree Days (°F)", min_value=1.0)
renewable = st.number_input("Total Renewable Energy (B Btu)", min_value=1.0)

# Prediction Button
if st.button("Predict"):

    # Log transform
    log_pop = np.log1p(population)
    log_cooling = np.log1p(cooling)
    log_renewable = np.log1p(renewable)

    # Centering
    pop_mean = 8.26120701813563
    cooling_mean = 2.5367094515610367

    pop_centered = log_pop - pop_mean
    cooling_centered = log_cooling - cooling_mean

    # Interaction
    pop_temp = pop_centered * cooling_centered

    # Feature array
    features = np.array([[log_pop, log_cooling, log_renewable, pop_temp]])

    # Scale
    features_scaled = scaler.transform(features)

    # Predict (log scale)
    pred_log = model.predict(features_scaled)

    # Convert to actual
    pred_actual = np.expm1(pred_log)

    st.success(f"Predicted Electricity Consumption: {pred_actual[0]:,.2f} GWh")